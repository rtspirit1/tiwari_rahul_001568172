    private void btnPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOrderActionPerformed
        String address=txtAddressField.getText();
        restaurant.addOrder(restaurant.getName(), userAccount.getUsername(), null, items, sum , address);
        for(Customer cust: ecosystem.getCustomerDirectory().getCustomerList()){
            if(userAccount.getUsername().equals(cust.getCustomerName())){
                cust.addOrder(restaurant.getName(), userAccount.getUsername(), null, items, sum , address);
                JOptionPane.showMessageDialog(null, "Order placed successfully");
            }
        }
        
        
    }//GEN-LAST:event_btnPlaceOrderActionPerformed

