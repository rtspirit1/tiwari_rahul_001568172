    private void btnAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCartActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblRestMenu.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from the table to add","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            Menu Item=(Menu)tblRestMenu.getValueAt(selectedRow, 0);

            populateCart(Item);

        }

    }//GEN-LAST:event_btnAddToCartActionPerformed

