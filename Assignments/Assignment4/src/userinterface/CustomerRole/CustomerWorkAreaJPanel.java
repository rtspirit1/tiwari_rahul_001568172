    public void populateCustTable(){
        DefaultTableModel tablemodel = (DefaultTableModel) tblRestMenu.getModel();
        
            tablemodel.setRowCount(0);
                Object[] row = new Object[3];
                for(Menu menu:restaurant.getMenu()){
                     row[0] = menu;
                     row[1] = menu.getDescription();
                     row[2] = menu.getPrice();
                     tablemodel.addRow(row);
                } 
        
    }
    
    public void populateCart(Menu dishitem){
        DefaultTableModel tablemodel = (DefaultTableModel) tblOrder.getModel();
        tablemodel.setRowCount(0);
        
         items.add(dishitem);
         Object[] row = new Object[4];
                for(Menu menu:items){
                     row[0] = menu;
                     row[1] = menu.getDescription();
                     row[2] = menu.getQuantity();
                     row[3] = menu.getPrice()*menu.getQuantity();
                    
                     sum=sum+(menu.getPrice()*menu.getQuantity());
                     tablemodel.addRow(row);
                }
    }

