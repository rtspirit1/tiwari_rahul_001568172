    public void populateMenuTable(){
        
        DefaultTableModel tablemodel = (DefaultTableModel) tblMenu.getModel();
        
        tablemodel.setRowCount(0);
        
       
        for (Restaurant rest:ecosystem.getRestaurantDirectory().getRestaurantList()) {
           
            if (rest.getUserName().equals(account.getUsername())) {
                System.out.println("1");
                if(!(rest.getMenu().isEmpty())){
                    for(Menu menu:rest.getMenu()){
                    System.out.println("2");
                    Object[] row = new Object[3];
                    row[0] = menu.getName();
                    row[1] = menu.getDescription();
                    row[2] = menu.getPrice();
                    tablemodel.addRow(row);
               }
                }
               
                
            }
            
        }
    }

